# Define two sets
set1 = {3, 65, 70}
set2 = {1, 3, 8, 15}

# Print the union of set1 and set2
# The union combines all unique elements from both sets.
print(set1.union(set2))

# Print the intersection of set1 and set2
# The intersection returns only the common elements in both sets.
print(set1.intersection(set2))